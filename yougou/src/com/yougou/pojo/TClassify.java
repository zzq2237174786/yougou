package com.yougou.pojo;

public class TClassify {
	private String tcId;
	private String tcName;
	private String tcParent;
	
	public TClassify() {
	}

	public String getTcId() {
		return tcId;
	}

	public void setTcId(String tcId) {
		this.tcId = tcId;
	}

	public String getTcName() {
		return tcName;
	}

	public void setTcName(String tcName) {
		this.tcName = tcName;
	}

	public String getTcParent() {
		return tcParent;
	}

	public void setTcParent(String tcParent) {
		this.tcParent = tcParent;
	}
	
	
}
