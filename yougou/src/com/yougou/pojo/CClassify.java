package com.yougou.pojo;

public class CClassify {
	private String ccId;
	private String ccName;
	private String ccParent;
	
	public CClassify() {
	}

	public String getCcId() {
		return ccId;
	}

	public void setCcId(String ccId) {
		this.ccId = ccId;
	}

	public String getCcName() {
		return ccName;
	}

	public void setCcName(String ccName) {
		this.ccName = ccName;
	}

	public String getCcParent() {
		return ccParent;
	}

	public void setCcParent(String ccParent) {
		this.ccParent = ccParent;
	}	
}
